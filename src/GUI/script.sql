CREATE TABLE PERSONA
(
    IDPERSONA INTEGER PRIMARY KEY  NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    NOMBRE VARCHAR(30) NOT NULL,
    FECHANAC   Date,
    SEXO   CHAR,
    ESTATURA  INT
);

CREATE TABLE MEDIDAS
(
        iDMEDIDAS INTEGER PRIMARY KEY NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
        FECHA Date,
        PESO      DOUBLE,
        CINTURA   INT,
        GRASACORPORAL DOUBLE,
    iDPERSONA INT 
    REFERENCES PERSONA (IDPERSONA)  ON DELETE NO ACTION ON UPDATE NO ACTION
); 
